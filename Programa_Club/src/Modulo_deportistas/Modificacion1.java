    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String dni = jTextField1.getText(); // Obtener el ID ingresado
       System.out.println("DNI ingresado: " + dni); // Verificar el valor del ID

    // Conexión a la base de datos
    Connection connection = null;
    PreparedStatement preparedStatement = null;
    ResultSet resultSet = null;

    try {
        
        // Cargar el controlador JDBC y obtener la conexión
        connection = DriverManager.getConnection("jdbc:mysql://bbbx7cdcbcl53xxmjyxb-mysql.services.clever-cloud.com:21748/bbbx7cdcbcl53xxmjyxb", "uwwqerjcglxxweor", "vWobxeLnCiH11WTJg6N");
        
        // Preparar la consulta SQL para verificar si el ID existe
        String sql = "SELECT * FROM Jugadores WHERE dni = ? and situacion='Alta'";
        preparedStatement = connection.prepareStatement(sql);
        preparedStatement.setString(1, dni);
        
        resultSet = preparedStatement.executeQuery();

        // Si existe un resultado, abrir la ventana de modificación
        if (resultSet.next()) {
            // Aquí pasas el ID a la ventana de modificación
            Modificacion newframe = new Modificacion(dni); // Pasar el ID como parámetro
            newframe.setVisible(true);
            newframe.setLocationRelativeTo(null);
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "El DNI no existe o esta dado de baja.");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al verificar el DNI: " + e.getMessage());
    } finally {
        // Cerrar la conexión y los recursos
        try {
            if (resultSet != null) resultSet.close();
            if (preparedStatement != null) preparedStatement.close();
            if (connection != null) connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

